<template>
  <div class="d-inline">
    <div class="position-relative d-inline">
      <SpinButton :variant="variant" :icon-name="icon" :label="label" class="mb-1" :spinclass="spinclass" :disabled="disabled" @handle="click" />
      <v-icon v-if="autosend" icon="chevron-circle-right" title="Autosend - configured to send immediately without edit" class="autosend" />
    </div>
    <ConfirmModal v-if="showDeleteModal" ref="deleteConfirm" :title="'Delete: ' + member.displayname" @confirm="deleteConfirmed" />
    <ModSpammerReport v-if="showSpamModal" ref="spamConfirm" :user="member" />
    <ModStdMessageModal v-if="showStdMsgModal" ref="stdmodal" :stdmsg="stdmsg" :member="member" :autosend="autosend" />
  </div>
</template>
<script>
import { useSpammerStore } from '../stores/spammer'

export default {
  setup() {
    const spammerStore = useSpammerStore()
    return { spammerStore }
  },

  props: {
    member: {
      type: Object,
      required: true
    },
    stdmsgid: {
      type: Number,
      required: false,
      default: null
    },
    variant: {
      type: String,
      required: true
    },
    label: {
      type: String,
      required: true
    },
    icon: {
      type: String,
      required: true
    },
    disabled: {
      type: Boolean,
      required: false,
      default: false
    },
    delete: {
      type: Boolean,
      required: false,
      default: false
    },
    release: {
      type: Boolean,
      required: false,
      default: false
    },
    spamreport: {
      type: Boolean,
      required: false,
      default: false
    },
    spamrequestremove: {
      type: Boolean,
      required: false,
      default: false
    },
    spamremove: {
      type: Boolean,
      required: false,
      default: false
    },
    spamconfirm: {
      type: Boolean,
      required: false,
      default: false
    },
    spamsafelist: {
      type: Boolean,
      required: false,
      default: false
    },
    spamhold: {
      type: Boolean,
      required: false,
      default: false
    },
    spamignore: {
      type: Boolean,
      required: false,
      default: false
    },
    leave: {
      type: Boolean,
      required: false,
      default: false
    },
    autosend: {
      type: Boolean,
      required: false,
      default: false
    }
  },
  data: function () {
    return {
      showDeleteModal: false,
      showStdMsgModal: false,
      showSpamModal: false,
      stdmsg: null
    }
  },
  computed: {
    groupid() {
      // For member entries this is returned at the top level.
      return this.member.groupid
    },
    spinclass() {
      if (this.variant === 'primary') {
        // Primary buttons have "success" (green) class.
        return 'success'
      }

      return null
    }
  },
  methods: {
    async click(callback) {
      if (this.approve) {
        // Standard approve button - no modal.
        await this.approveIt()
      } else if (this.delete) {
        // Standard delete button - no modal.
        await this.deleteIt()
      } else if (this.spamreport) {
        await this.spamReport()
      } else if (this.spamconfirm) {
        await this.spamConfirm()
      } else if (this.spamrequestremove) {
        await this.spamRequestRemove()
      } else if (this.spamremove) {
        await this.spamRemove()
      } else if (this.spamsafelist) {
        await this.spamSafelist()
      } else if (this.spamhold) {
        await this.spamHold()
      } else if (this.spamignore) {
        await this.spamIgnore()
      } else if (this.release) {
        console.log('Release')
        await this.releaseIt()
      } else {
        // We want to show a modal.
        if (this.leave) {
          this.stdmsg = {
            action: 'Leave Member'
          }
        } else if (this.stdmsgid) {
          // We have a standard message.  Fetch it.
          alert("TODO MMB1")
